package com.diana.storyplanner.entity;

/**
 * Перечисление жанров историй.
 * Значения хранятся в нижнем регистре для соответствия базе данных.
 */
public enum Genre {
    /** Фэнтези — магия, вымышленные миры */
    fantasy,

    /** Научная фантастика — технологии, будущее */
    sci_fi,

    /** Ужасы — страх, сверхъестественное */
    horror,

    /** Детектив — расследование преступлений */
    detective,

    /** Триллер — напряжение, опасность */
    thriller,

    /** Драма — эмоции, жизненные ситуации */
    drama,

    /** Романтика — любовные отношения */
    romance,

    /** Приключения — путешествия, поиски */
    adventure,

    /** Боевик — экшен, сражения */
    action,

    /** Мистика — загадочное, непознанное */
    mystic,

    /** Исторический — события прошлого */
    historical,

    /** Криминал — преступный мир */
    crime,

    /** Комедия — юмор, смешные ситуации */
    comedy
}